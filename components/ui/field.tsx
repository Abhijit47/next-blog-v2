/**
 * Renders a horizontal divider for form fields with an optional centered content badge.
 *
 * When `children` is provided, the content is displayed in a centered span between the separators.
 *
 * @param children - Optional content to render centered within the separator.
 * @param className - Additional CSS classes to apply to the wrapper element.
 * @returns A wrapper `div` containing a `Separator` and, if present, a centered content span.
 */
function FieldSeparator({
  children,
  className,
  ...props
}: React.ComponentProps<"div"> & {
  children?: React.ReactNode
}) {
  return (
    <div
      data-slot="field-separator"
      data-content={!!children}
      className={cn(
        "relative -my-2 h-5 text-sm group-data-[variant=outline]/field-group:-mb-2",
        className
      )}
      {...props}
    >
      <Separator className="absolute inset-0 top-1/2" />
      {children && (
        <span
          className="bg-background text-muted-foreground relative mx-auto block w-fit px-2"
          data-slot="field-separator-content"
        >
          {children}
        </span>
      )}
    </div>
  )
}

/**
 * Renders field-level error content as an alert when `children` or `errors` are provided.
 *
 * If `children` is present it is used verbatim. If `errors` contains a single item its message is rendered;
 * if it contains multiple items the messages are rendered as a list. If neither produces content, nothing is rendered.
 *
 * @param children - Optional custom content to render inside the error container; takes precedence over `errors`.
 * @param errors - Optional array of error objects; each item may include a `message` string to display.
 * @returns The error container element with `role="alert"` and destructive styling when content exists, `null` otherwise.
 */
function FieldError({
  className,
  children,
  errors,
  ...props
}: React.ComponentProps<"div"> & {
  errors?: Array<{ message?: string } | undefined>
}) {
  const content = useMemo(() => {
    if (children) {
      return children
    }

    if (!errors?.length) {
      return null
    }

    if (errors?.length == 1) {
      return errors[0]?.message
    }

    return (
      <ul className="ml-4 flex list-disc flex-col gap-1">
        {errors.map(
          (error, index) =>
            error?.message && <li key={index}>{error.message}</li>
        )}
      </ul>
    )
  }, [children, errors])

  if (!content) {
    return null
  }

  return (
    <div
      role="alert"
      data-slot="field-error"
      className={cn("text-destructive text-sm font-normal", className)}
      {...props}
    >
      {content}
    </div>
  )
}

